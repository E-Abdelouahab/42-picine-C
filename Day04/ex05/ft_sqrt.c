int	ft_sqrt(int nb)
{
	int	count;

	if (nb <= 0)
	{
		return (0);
	}
	count = 1;
	while (count * count <= nb)
	{
		if (count * count == nb)
		{
			return (count);
		}
		else
		{
			count++;
		}
	}
	return (0);
}
