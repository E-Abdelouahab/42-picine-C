char	*ft_strncat(char *dest, char *src, int nb)
{
	char	*ptr;

	ptr = dest;
	while (*ptr != '\0')
		ptr++;
	while (nb > 0)
	{
		*ptr = *src;
		nb--;
		src++;
		ptr++;
	}
	*ptr = '\0';
	return (dest);
}
