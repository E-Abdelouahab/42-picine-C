int		ft_any(char **tab, int (*f)(char*))
{
	int	i;

	if (f == 0 || tab == (char**)0)
		return (0);
	i = 0;
	while (tab[i] != (char*)0)
	{
		if (f(tab[i]))
			return (1);
		i++;
	}
	return (0);
}
