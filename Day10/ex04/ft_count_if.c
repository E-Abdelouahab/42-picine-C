int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	count;

	if (f == 0 || tab == (char**)0)
		return (0);
	i = 0;
	count = 0;
	while (tab[i] != (char*)0)
	{
		if (f(tab[i]))
			count++;
		i++;
	}
	return (count);
}
